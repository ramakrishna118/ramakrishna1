import java.util.*;

public class DataTypes
{

	public static void main(String[] args) 
	{
		
		char a = 'R';
		
		int i = 10;
		
		boolean b = true;
		
		double d = 4.5487;
		
		float f = 3.1458f;
		
		System.out.println("char:"+a);
		System.out.println("int:"+i);
		System.out.println("boolen:"+b);
		System.out.println("double:"+d);
		System.out.println("float:"+f);

	}

}
